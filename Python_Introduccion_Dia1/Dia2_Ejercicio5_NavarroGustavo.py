# Algoritmo para convertir Celsius a Fahrenheit

# Se le pide al usuario los grados que desea convertir
celsius = float(input("Ingrese los Grados Celcius"))

# Logica para sacar el resultado de la conversion
fahrenheit = (celsius * 9/5) + 32

# imprimir el resultado
print(celsius,"°C es igual a ",fahrenheit,"°F")

