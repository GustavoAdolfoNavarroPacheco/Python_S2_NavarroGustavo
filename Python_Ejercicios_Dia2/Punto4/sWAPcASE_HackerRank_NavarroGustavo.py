# Entrada: Texto ingresado por el usuario
# Salida: Texto ingresado por el usuario pero con la Mayusculas en Minusculas y viceversa

def swap_case(s):
    return s.swapcase()

if __name__ == '__main__':
    s = input()
    result = swap_case(s)
    print(result)

